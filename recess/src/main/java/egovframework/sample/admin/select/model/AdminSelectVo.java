package egovframework.sample.admin.select.model;

public class AdminSelectVo {

	String idx = "";
	String seq = "";
	String question_idx = "";
	String content = "";
	String image_boolean = "";
	String image = "";
	String create_tm = "";
	String update_tm = "";
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getQuestion_idx() {
		return question_idx;
	}
	public void setQuestion_idx(String question_idx) {
		this.question_idx = question_idx;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImage_boolean() {
		return image_boolean;
	}
	public void setImage_boolean(String image_boolean) {
		this.image_boolean = image_boolean;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getCreate_tm() {
		return create_tm;
	}
	public void setCreate_tm(String create_tm) {
		this.create_tm = create_tm;
	}
	public String getUpdate_tm() {
		return update_tm;
	}
	public void setUpdate_tm(String update_tm) {
		this.update_tm = update_tm;
	}
	
	
	
}
