package egovframework.sample.menu.model;

public class MenuVo {

	String idx = "";
	String type = "";
	String depth = "";
	String title = "";
	String upper_menu_idx = "";
	String seq = "";
	String name = "";
	String link = "";
	String create_tm = "";
	String update_tm = "";
	
	String upper_title = "";

	public String getIdx() {
		return idx;
	}

	public void setIdx(String idx) {
		this.idx = idx;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getDepth() {
		return depth;
	}

	public void setDepth(String depth) {
		this.depth = depth;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUpper_menu_idx() {
		return upper_menu_idx;
	}

	public void setUpper_menu_idx(String upper_menu_idx) {
		this.upper_menu_idx = upper_menu_idx;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getCreate_tm() {
		return create_tm;
	}

	public void setCreate_tm(String create_tm) {
		this.create_tm = create_tm;
	}

	public String getUpdate_tm() {
		return update_tm;
	}

	public void setUpdate_tm(String update_tm) {
		this.update_tm = update_tm;
	}

	public String getUpper_title() {
		return upper_title;
	}

	public void setUpper_title(String upper_title) {
		this.upper_title = upper_title;
	}
	
	
	
	
	
	
}
