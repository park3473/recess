package egovframework.sample.admin.product.model;

public class AdminProductListVo {
	
	String idx = "";
	String exam_idx = "";
	String pro_idx = "";
	String min_score = "";
	String max_score = "";
	String recess = "";
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getExam_idx() {
		return exam_idx;
	}
	public void setExam_idx(String exam_idx) {
		this.exam_idx = exam_idx;
	}
	public String getPro_idx() {
		return pro_idx;
	}
	public void setPro_idx(String pro_idx) {
		this.pro_idx = pro_idx;
	}
	public String getMin_score() {
		return min_score;
	}
	public void setMin_score(String min_score) {
		this.min_score = min_score;
	}
	public String getMax_score() {
		return max_score;
	}
	public void setMax_score(String max_score) {
		this.max_score = max_score;
	}
	public String getRecess() {
		return recess;
	}
	public void setRecess(String recess) {
		this.recess = recess;
	}
	
	

}
