package egovframework.sample.admin.question.model;

public class AdminQuestionListVo {
	
	String idx = "";
	String exam_idx = "";
	String seq = "";
	String question_idx = "";
	String create_tm = "";
	String update_tm = "";
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getExam_idx() {
		return exam_idx;
	}
	public void setExam_idx(String exam_idx) {
		this.exam_idx = exam_idx;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getQuestion_idx() {
		return question_idx;
	}
	public void setQuestion_idx(String question_idx) {
		this.question_idx = question_idx;
	}
	public String getCreate_tm() {
		return create_tm;
	}
	public void setCreate_tm(String create_tm) {
		this.create_tm = create_tm;
	}
	public String getUpdate_tm() {
		return update_tm;
	}
	public void setUpdate_tm(String update_tm) {
		this.update_tm = update_tm;
	}
	
	

}
